/**
 *  This class is used to transfer from sensors to Managers
 */

public class Info {
    private String name;
    private int value;

    public Info(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
